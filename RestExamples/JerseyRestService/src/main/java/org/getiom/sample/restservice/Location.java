package org.getiom.sample.restservice;


public class Location {
    private Double x;
    private Double y;

    public Location() {}

    public Location(Double x, Double y) {
        this.x = x;
        this.y = y;
    }

    public Double getX() {
        return x;
    }

    public void setX(Double x) {
        this.x = x;
    }

    public Double getY() {
        return y;
    }

    public void setY(Double y) {
        this.y = y;
    }
}

